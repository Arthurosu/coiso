# variáveis ---> São como caixinhas que guardam informações.
# Existem vários tipos de informação e por isso vários tipos de variáveis.

# As variáveis podem ter qualquer nome que o desenvolvedor escolher:
batata = "batata"

# O "=" na criação de uma variável não significa o mesmo que na matemática,
# ele quer dizer que você está colocando esse valor dentro da caixinho que é a variável.

# Variáveis do tipo Int são variáveis onde podemos colocar número inteiros,
# tanto positivos quanto negativos.
variavel_numero_inteiro = 5

# Variáveis do tipo Float são variáveis onde podemos colocar números Reais,
# ou seja, números com vírgula, podendo ser positivos ou negativos.
variavel_numero_quebrado = 5.5

# Variáveis do tipo String são variáveis onde podemos colocar qualquer caractér,
# ou seja, letra, número ou símbolo e será lida como texto.
variavel_texto = "Olá Mundo"

# Variáveis do tipo Boolean são variáveis onde podemos colocar um valor de "falso" ou "verdadeiro" apenas.
variavel_verdadeiro_falso = True

